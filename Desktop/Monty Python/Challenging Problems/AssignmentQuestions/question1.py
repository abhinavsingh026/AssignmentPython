intnum = 12
print('Integer: ',intnum)
print('Type: ',type(intnum))
floatnum = 12.25
print('Float: ',floatnum)
print('Type: ',type(floatnum))
complexnum = 2.5 + 3.5j
print('Complex: ',complexnum)
print('Complex Real part: ',complexnum.real)
print('Complex Imaginary part: ',complexnum.imag)
print('Type: ',type(complexnum))